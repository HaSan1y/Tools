from PIL import Image, ImageDraw, ImageFont

# Create a blank image
width, height = 600, 300
image = Image.new("RGB", (width, height), "white")
draw = ImageDraw.Draw(image)

# Draw breadboard power rails
draw.rectangle([50, 20, 550, 40], fill="#FFCCCC")  # Positive rail
draw.text((10, 25), "+", fill="red")
draw.rectangle([50, 260, 550, 280], fill="#CCCCFF")  # Negative rail
draw.text((10, 265), "-", fill="blue")

# Draw columns (simplified)
for i in range(20):
    x = 50 + i * 25
    draw.line((x, 60, x, 240), fill="lightgray")

# Draw center gap
draw.rectangle([290, 60, 310, 240], fill="white")

# Draw LED (anode to cathode)
draw.ellipse([225, 100, 235, 110], fill="red")  # Anode
draw.text((240, 95), "LED", fill="black")
draw.line((230, 105, 230, 145), fill="black", width=2)  # To resistor

# Draw resistor
