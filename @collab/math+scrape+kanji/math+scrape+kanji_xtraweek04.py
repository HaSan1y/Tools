import asyncio
from crawl4ai import AsyncWebCrawler
import nest_asyncio
import pandas as pd
from bs4 import BeautifulSoup

# Apply nest_asyncio to enable running async code within Jupyter
nest_asyncio.apply()

# Install the required browser for Playwright
# !playwright install


async def main():
    # Create an instance of AsyncWebCrawler
    async with AsyncWebCrawler() as crawler:
        # Run the crawler on a URL
        result = await crawler.arun(url="https://crawl4ai.com/")

        # Print the extracted content .content
        # print(result.markdown)

        # Parse the HTML content using BeautifulSoup
        soup = BeautifulSoup(result.html, 'html.parser')

        # Find all 'p' tags and extract their text
        data = []
        for p_tag in soup.find_all('p'):
            data.append({'text': p_tag.text})  # Store in a list of dictionaries

        # Create a Pandas DataFrame from the extracted data
        df = pd.DataFrame(data)

        # Export the DataFrame to a CSV file
        df.to_csv('extracted_text.csv', index=False)
        print("Data saved to extracted_text.csv")

# Run the async main function
asyncio.run(main())

